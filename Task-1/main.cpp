/* A program to calculate the area and perimeter
 * of a rectangle given the length and the width 
 * created by Aly Alsayed
 * */
#include<bits/stdc++.h>
using namespace std;
 
int main()
{
    int length,width;// two variable to store 
    cin>>length>>width;// get the input 
    cout<<(length*width)<<endl;//output the area
    cout<<(length+width)*2 ;//output the perimeter
}
